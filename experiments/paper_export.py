def export_research_paper_artifacts(*, output_root: str = "result", latex_root: str = "latex") -> dict[str, Any]:
    """Regenerate LaTeX tables/macros from canonical results and manifests."""

    aggregate_research_results(output_root=output_root)
    result_root = Path(output_root)
    latex = Path(latex_root)
    tables = latex / "tables" / "generated"
    tables.mkdir(parents=True, exist_ok=True)
    source_path = result_root / "splits" / "harmeme" / "source_split_seed_42.json"
    fhm_path = result_root / "splits" / "fhm" / "heldout_test_manifest.json"
    source = load_manifest(source_path) if source_path.exists() else {}
    fhm = load_manifest(fhm_path) if fhm_path.exists() else {}
    train_count = len(source.get("train", []) or []) if source else None
    valid_count = len(source.get("validation", []) or []) if source else None
    test_count = len(fhm.get("test", []) or []) if fhm else None
    macros = [
        "% Generated by experiments.paper_export. Do not hand-edit.",
        f"\\providecommand{{\\PaperStatus}}{{{'Results available' if _has_completed_results(result_root) else 'Not run'}}}",
        f"\\providecommand{{\\SourceTrainCount}}{{{_display(train_count)}}}",
        f"\\providecommand{{\\SourceValidationCount}}{{{_display(valid_count)}}}",
        f"\\providecommand{{\\FHMTestCount}}{{{_display(test_count)}}}",
        "\\providecommand{\\OursFHMMacroFOne}{\\texttt{TBD}}",
        "\\providecommand{\\BestBaselineName}{\\texttt{TBD}}",
        "\\providecommand{\\OursImprovement}{\\texttt{TBD}}",
        "\\providecommand{\\PaperDraftPlaceholder}[1]{\\textit{[Draft status: #1]}}",
    ]
    (latex / "generated" / "result_macros.tex").write_text("\n".join(macros) + "\n", encoding="utf-8")
    (tables / "supp_per_dataset.tex").write_text(_dataset_table(train_count, valid_count, test_count), encoding="utf-8")
    summaries = _read_csv(result_root / "aggregates" / "results_mean_std.csv")
    statuses = _read_csv(result_root / "aggregates" / "experiment_status.csv")
    result_macros = _result_macros(summaries)
    macros[5:8] = result_macros
    (latex / "generated" / "result_macros.tex").write_text("\n".join(macros) + "\n", encoding="utf-8")
    (tables / "main_baseline.tex").write_text(_metric_table(summaries, task="harmfulness", groups={"built_in_baseline", "proposed"}), encoding="utf-8")
    (tables / "main_structured.tex").write_text(_structured_table(summaries), encoding="utf-8")
    (tables / "main_core_ablation.tex").write_text(_metric_table(summaries, task="harmfulness", groups={"core_ablation"}), encoding="utf-8")
    (tables / "supp_per_seed.tex").write_text(_per_seed_status_table(statuses), encoding="utf-8")
    (tables / "supp_coverage.tex").write_text(_coverage_table(_read_csv(result_root / "aggregates" / "coverage_results.csv")), encoding="utf-8")
    (tables / "supp_external_baselines.tex").write_text(_external_table(_read_csv(result_root / "aggregates" / "literature_reported_results.csv")), encoding="utf-8")
    (latex / "generated" / "experiment_status.tex").write_text(_status_table(statuses), encoding="utf-8")
    registry = load_experiment_registry()
    required = {
        experiment_id: spec.get("status")
        for experiment_id, spec in (registry.get("experiments", {}) or {}).items()
        if spec.get("priority") == "required"
    }
    completed = {row.get("experiment_id") for row in statuses if row.get("status") == "complete" and str(row.get("audit_passed")).lower() == "true"}
    required_status = {
        experiment_id: "completed" if experiment_id in completed else status
        for experiment_id, status in required.items()
    }
    generated_figures = _generate_figures(latex, summaries, statuses)
    manifest = {
        "schema_version": "paper_export_manifest_v1",
        "source_split_sha256": sha256_file(source_path) if source_path.exists() else None,
        "fhm_test_sha256": sha256_file(fhm_path) if fhm_path.exists() else None,
        "results_sha256": sha256_file(result_root / "aggregates" / "results_mean_std.csv") if (result_root / "aggregates" / "results_mean_std.csv").exists() else None,
        "generated_files": sorted(
            str(path)
            for base in (latex / "generated", latex / "tables" / "generated", latex / "figures" / "generated")
            for path in base.rglob("*")
            if path.is_file()
        ),
        "generated_figures": generated_figures,
        "required_experiment_status": required_status,
        "missing_required_experiments": sorted(key for key, status in required_status.items() if status != "completed"),
        "fabricated_numeric_placeholders": False,
    }
    write_json(latex / "generated" / "generation_manifest.json", manifest)
    return manifest


def check_paper(*, mode: str = "draft", latex_root: str = "latex", results_root: str = "result") -> dict[str, Any]:
    """Check scaffold completeness; final mode rejects all draft markers."""

    if mode not in {"draft", "final"}:
        raise ValueError("mode must be draft or final")
    root = Path(latex_root)
    required = [
        root / "main.tex",
        root / "reference.bib",
        root / "generated" / "result_macros.tex",
        root / "generated" / "experiment_status.tex",
        root / "generated" / "generation_manifest.json",
        root / "tables" / "generated" / "main_baseline.tex",
        root / "tables" / "generated" / "main_core_ablation.tex",
        root / "tables" / "generated" / "main_structured.tex",
    ]
    errors = [f"missing required file: {path}" for path in required if not path.exists()]
    markers = []
    pattern = re.compile(r"PaperDraftPlaceholder|TODO_RESULT|\bTBD\b|MISSING_RESULT|\bNaN\b|Not run", re.IGNORECASE)
    for path in sorted(root.rglob("*.tex")):
        for line_no, line in enumerate(path.read_text(encoding="utf-8", errors="ignore").splitlines(), start=1):
            if pattern.search(line):
                markers.append({"path": str(path), "line": line_no, "text": line.strip()[:180]})
    if mode == "final" and markers:
        errors.append(f"final mode found {len(markers)} draft/blocked markers")
    preflight = _read_json(Path(results_root) / "research_planning" / "protocol_preflight.json")
    generation = _read_json(root / "generated" / "generation_manifest.json")
    if mode == "final":
        if not preflight.get("passed"):
            errors.append("strict research preflight is missing or failed")
        missing_required = generation.get("missing_required_experiments", []) or []
        if missing_required:
            errors.append(f"required experiments are incomplete: {missing_required}")
    return {
        "mode": mode,
        "passed": not errors,
        "errors": errors,
        "draft_markers": markers,
        "strict_preflight_status": preflight.get("status", "not_run"),
        "missing_required_experiments": generation.get("missing_required_experiments", []),
    }


def _metric_table(rows: list[dict[str, str]], *, task: str, groups: set[str] | None) -> str:
    selected = [row for row in rows if row.get("task") == task and row.get("metric") in {"macro_f1", "roc_auc"} and (groups is None or row.get("group") in groups)]
    by_model: dict[str, dict[str, str]] = {}
    for row in selected:
        by_model.setdefault(row.get("model", row.get("experiment_id", "")), {})[row.get("metric", "")] = _mean_std(row)
    if not by_model:
        by_model = {"Not run": {"macro_f1": "--", "roc_auc": "--"}}
    lines = ["\\begin{tabular}{lcc}", "\\toprule", "Method & Macro-F1 & ROC-AUC \\\\", "\\midrule"]
    lines.extend(f"{_tex(name)} & {values.get('macro_f1', '--')} & {values.get('roc_auc', '--')} \\\\" for name, values in sorted(by_model.items()))
    return "\n".join(lines + ["\\bottomrule", "\\end{tabular}", ""])


def _structured_table(rows: list[dict[str, str]]) -> str:
    tasks = ["target_presence", "intent_primary", "tactic_multimodal_relation"]
    by_model: dict[str, dict[str, str]] = {}
    for row in rows:
        if row.get("task") in tasks and row.get("metric") == "macro_f1":
            by_model.setdefault(row.get("model", ""), {})[row["task"]] = _mean_std(row)
    if not by_model:
        by_model = {"Not run": {}}
    lines = ["\\begin{tabular}{lccc}", "\\toprule", "Method & Target & Intent & Tactic \\\\", "\\midrule"]
    lines.extend(f"{_tex(name)} & {values.get(tasks[0], '--')} & {values.get(tasks[1], '--')} & {values.get(tasks[2], '--')} \\\\" for name, values in sorted(by_model.items()))
    return "\n".join(lines + ["\\bottomrule", "\\end{tabular}", ""])


def _dataset_table(train: int | None, valid: int | None, test: int | None) -> str:
    return "\n".join(["\\begin{tabular}{lr}", "\\toprule", "Split & Count \\\\", "\\midrule", f"HarMeme train & {_display(train)} \\\\", f"HarMeme validation & {_display(valid)} \\\\", f"FHM held-out test & {_display(test)} \\\\", "\\bottomrule", "\\end{tabular}", ""])


def _status_table(rows: list[dict[str, str]]) -> str:
    selected = rows[:30] or [{"experiment_id": "Built-in suite", "status": "Not run"}]
    lines = ["\\begin{tabular}{ll}", "\\toprule", "Experiment & Status \\\\", "\\midrule"]
    lines.extend(f"{_tex(row.get('experiment_id', '--'))} & {_tex(row.get('status', '--'))} \\\\" for row in selected)
    return "\n".join(lines + ["\\bottomrule", "\\end{tabular}", ""])


def _per_seed_status_table(rows: list[dict[str, str]]) -> str:
    selected = rows[:50] or [{"experiment_id": "Not run", "seed": "--", "status": "Not run"}]
    lines = ["\\begin{tabular}{lcc}", "\\toprule", "Method & Seed & Status \\\\", "\\midrule"]
    lines.extend(f"{_tex(row.get('experiment_id', '--'))} & {_tex(row.get('seed', '--'))} & {_tex(row.get('status', '--'))} \\\\" for row in selected)
    return "\n".join(lines + ["\\bottomrule", "\\end{tabular}", ""])


def _coverage_table(rows: list[dict[str, str]]) -> str:
    selected = rows[:50] or [{"task": "Silver structured evaluation", "valid_n": "--", "coverage": "--"}]
    lines = ["\\begin{tabular}{lcc}", "\\toprule", "Task & Valid N & Coverage \\\\", "\\midrule"]
    lines.extend(f"{_tex(row.get('task', '--'))} & {_tex(row.get('valid_n', '--'))} & {_tex(row.get('coverage', '--'))} \\\\" for row in selected)
    return "\n".join(lines + ["\\bottomrule", "\\end{tabular}", ""])


def _external_table(rows: list[dict[str, str]]) -> str:
    selected = rows[:50] or [{"display_name": "Feasibility registry", "status": "Blocked"}]
    lines = ["\\begin{tabular}{lc}", "\\toprule", "External method & Status \\\\", "\\midrule"]
    lines.extend(f"{_tex(row.get('display_name', '--'))} & {_tex(row.get('status', '--'))} \\\\" for row in selected)
    return "\n".join(lines + ["\\bottomrule", "\\end{tabular}", ""])


def _read_csv(path: Path) -> list[dict[str, str]]:
    if not path.exists() or not path.read_text(encoding="utf-8").strip():
        return []
    with path.open("r", encoding="utf-8", newline="") as handle:
        return list(csv.DictReader(handle))


def _mean_std(row: dict[str, str]) -> str:
    mean_value = row.get("mean")
    std_value = row.get("std")
    try:
        rendered = f"{float(mean_value):.3f}"
        if std_value not in {None, "", "None"}:
            rendered += f" $\\pm$ {float(std_value):.3f}"
        return rendered
    except (TypeError, ValueError):
        return "--"


def _display(value: Any) -> str:
    return "--" if value is None else str(value)


def _tex(value: Any) -> str:
    return str(value).replace("_", "\\_").replace("&", "\\&")


def _has_completed_results(root: Path) -> bool:
    return bool(_read_csv(root / "aggregates" / "results_mean_std.csv"))


def _result_macros(rows: list[dict[str, str]]) -> list[str]:
    ours = next((row for row in rows if row.get("experiment_id") == "ours_full" and row.get("task") == "harmfulness" and row.get("metric") == "macro_f1"), None)
    baselines = [row for row in rows if row.get("group") == "built_in_baseline" and row.get("task") == "harmfulness" and row.get("metric") == "macro_f1"]
    best = max(baselines, key=lambda row: _number(row.get("mean"), float("-inf")), default=None)
    ours_value = _number(ours.get("mean") if ours else None, None)
    best_value = _number(best.get("mean") if best else None, None)
    improvement = ours_value - best_value if ours_value is not None and best_value is not None else None
    return [
        f"\\providecommand{{\\OursFHMMacroFOne}}{{{f'{ours_value:.3f}' if ours_value is not None else '\\texttt{TBD}'}}}",
        f"\\providecommand{{\\BestBaselineName}}{{{_tex(best.get('model')) if best else '\\texttt{TBD}'}}}",
        f"\\providecommand{{\\OursImprovement}}{{{f'{improvement:.3f}' if improvement is not None else '\\texttt{TBD}'}}}",
    ]


def _generate_figures(latex: Path, summaries: list[dict[str, str]], statuses: list[dict[str, str]]) -> list[str]:
    """Generate PDFs only when compatible numeric data exists."""

    figure_root = latex / "figures" / "generated"
    generated: list[str] = []
    main = [row for row in summaries if row.get("task") == "harmfulness" and row.get("metric") == "macro_f1" and _number(row.get("mean"), None) is not None]
    if not main:
        return generated
    try:
        import matplotlib.pyplot as plt  # type: ignore
    except ImportError:
        return generated
    figure_root.mkdir(parents=True, exist_ok=True)
    labels = [row.get("experiment_id", "") for row in main]
    values = [_number(row.get("mean"), 0.0) for row in main]
    fig, ax = plt.subplots(figsize=(max(5, len(labels) * 0.65), 3.2))
    ax.bar(range(len(labels)), values, color="#136f63")
    ax.set_ylabel("Macro-F1")
    ax.set_xticks(range(len(labels)), labels, rotation=45, ha="right")
    fig.tight_layout()
    path = figure_root / "main_performance.pdf"
    fig.savefig(path)
    plt.close(fig)
    generated.append(str(path))
    return generated


def _number(value: Any, default: Any) -> Any:
    try:
        return float(value)
    except (TypeError, ValueError):
        return default


def _read_json(path: Path) -> dict[str, Any]:
    if not path.exists():
        return {}
    try:
        value = json.loads(path.read_text(encoding="utf-8"))
        return value if isinstance(value, dict) else {}
    except (OSError, json.JSONDecodeError):
        return {}


